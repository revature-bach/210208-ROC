package com.revature.main;

public class HelloWorld {

	// This is the main method. It is a special method that the JVM looks for whenever we execute our program
	// so if we, for example, ran this class, what's actually running first of all would be the main method
	// So you can think of the main method as an entryway into the actual running of our application
	public static void main(String[] args) {
		System.out.println("Hello World!");
	}

}
